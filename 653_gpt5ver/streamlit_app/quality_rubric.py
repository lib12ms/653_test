def empty_eval_fields() -> dict[str, str]:
    return {c: "" for c in EVAL_COLUMNS}


def merge_machine_and_eval_slots(machine: dict[str, object]) -> dict[str, object]:
    """기계 산출 행에 빈 평가 칸을 붙입니다."""
    row = dict(machine)
    row.update(empty_eval_fields())
    return row
