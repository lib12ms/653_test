def _extract_category_candidates(category: str, content_code: str = "") -> list[str]:
    """분류 체인의 구체 하위 분야명을 보강 후보로 사용한다."""
    category_group = get_category_group(category, content_code)
    candidates: list[str] = []
    for part in reversed([p.strip() for p in (category or "").split(">") if p.strip()]):
        if "국립중앙도서관" in part or "kdc" in norm_text(part):
            continue
        if "/" in part:
            continue
        token = re.sub(r'\([^)]*\)', '', part.replace(" ", "")).strip()
        n = norm_text(token).replace(" ", "")
        if len(token) < 2 or len(token) > 10:
            continue
        if n in CATEGORY_CANDIDATE_DENY:
            continue
        if _is_low_value_keyword(n, category_group):
            continue
        candidates.append(token)

    # 유효 후보가 없으면 (국가+장르 필터 등으로 모두 차단된 경우) 장르 대체어 사용
    # 카테고리 오른쪽(구체) → 왼쪽(일반) 순서로 장르를 탐색
    if not candidates:
        parts = [p.strip() for p in category.split(">") if p.strip()]
        for part in reversed(parts):
            part_compact = part.replace(" ", "")
            for genre, fallbacks in _GENRE_FALLBACKS.items():
                if genre in part_compact:
                    candidates.extend(fallbacks)
                    break
            if candidates:
                break

    return candidates


_AI_TOPIC_RE = re.compile(r"(인공지능|ai)")
MAX_AI_TOPIC_KEYWORDS = 2


def _is_ai_topic_keyword(normalized_keyword: str) -> bool:
    """'인공지능'·'AI'가 포함된 키워드인지(과잉 사용 개수 제한용)."""
    return bool(_AI_TOPIC_RE.search(normalized_keyword))


def finalize_653(
    ai_output: str,
    forbidden_set: set[str],
    max_keywords: int = 7,
    min_keywords: int = 5,
    category: str = "",
    toc: str = "",
    description: str = "",
    content_code: str = "",
) -> tuple[str, Field653Quality]:
    """AI 출력에서 금지어·저효용어를 제거하고 $a 형식과 품질 지표를 함께 반환."""
    keywords = [k.strip() for k in ai_output.split("$a") if k.strip()]
    ai_raw_count = len(keywords)

    author_bio_like = {
        "작가", "저자", "등단", "수상", "작품세계", "문단", "생애", "인터뷰", "연보", "약력",
    }
    cat_norm = norm_text(category)
    allow_bio = any(t in cat_norm for t in ("전기", "평전", "작가론", "인물", "회고록"))
    category_group = get_category_group(category, content_code)

    valid_keywords: list[str] = []
    blocked_kws: list[str] = []
    seen: set[str] = set()
    ai_topic_count = 0
    for kw in keywords:
        if not validate_keyword(kw, forbidden_set):
            blocked_kws.append(kw)
            continue
        n = norm_text(kw)
        if _is_low_value_keyword(n, category_group):
            blocked_kws.append(kw)
            continue
        if not allow_bio and any(b in n for b in author_bio_like):
            blocked_kws.append(kw)
            continue
        if n in seen:
            continue  # 중복 — blocked가 아니라 무시
        if _is_ai_topic_keyword(n):
            if ai_topic_count >= MAX_AI_TOPIC_KEYWORDS:
                blocked_kws.append(kw)
                continue
            ai_topic_count += 1
        seen.add(n)
        valid_keywords.append(kw.replace(" ", ""))

    ai_valid_count = len(valid_keywords)
    filtered_count = ai_raw_count - ai_valid_count
    backup_used = ai_valid_count == 0  # AI 유효 키워드가 0개인지 여부
    text_fallback_used = False  # 실제로 텍스트 fallback이 키워드를 추가했는지
    _ai_valid_boundary = ai_valid_count  # fallback 시작 경계 (나중에 슬라이싱용)

    # 문학은 텍스트 토크나이즈 fallback 금지 — 5유형 비구조 토큰(주인공·서울·이야기 등)이 삽입됨
    # 카테고리 fallback(_extract_category_candidates)이 장르 대체어를 제공하므로 충분함
    if backup_used and category_group != "문학":
        _count_before_text = len(valid_keywords)
        backup = _extract_backup_candidates(category, toc, description)
        for kw in backup:
            n = norm_text(kw)
            if n in seen:
                continue
            if _is_low_value_keyword(n, category_group):
                continue
            if not validate_keyword(kw, forbidden_set):
                continue
            if not allow_bio and any(b in n for b in author_bio_like):
                continue
            if _is_ai_topic_keyword(n):
                if ai_topic_count >= MAX_AI_TOPIC_KEYWORDS:
                    continue
                ai_topic_count += 1
            seen.add(n)
            valid_keywords.append(kw)
            if len(valid_keywords) >= min_keywords:
                break
        text_fallback_used = len(valid_keywords) > _count_before_text

    _count_before_fallback = len(valid_keywords)
    if _count_before_fallback < min_keywords:
        for kw in _extract_category_candidates(category, content_code):
            n = norm_text(kw)
            if n in seen:
                continue
            if _is_low_value_keyword(n, category_group):
                continue
            if not validate_keyword(kw, forbidden_set):
                continue
            if _is_ai_topic_keyword(n):
                if ai_topic_count >= MAX_AI_TOPIC_KEYWORDS:
                    continue
                ai_topic_count += 1
            seen.add(n)
            valid_keywords.append(kw)
            if len(valid_keywords) >= min_keywords:
                break
    # 카테고리 fallback이 실제로 키워드를 추가한 경우에만 플래그/페널티 적용
    category_fallback_used = len(valid_keywords) > _count_before_fallback

    final_count = len(valid_keywords[:max_keywords])

    # 품질 점수 산출
    flags: list[str] = []
    if ai_raw_count < 3:
        flags.append("AI생성부족")
    filter_rate = filtered_count / ai_raw_count if ai_raw_count > 0 else 0.0
    if filter_rate > 0.5:
        flags.append("과다차단")
    if text_fallback_used:
        flags.append("텍스트fallback사용")
    if category_fallback_used:
        flags.append("카테고리fallback사용")
    if final_count < min_keywords:
        flags.append("키워드부족")

    score = min(final_count, max_keywords) / max(max_keywords, 1)
    if filter_rate > 0.3:
        score -= (filter_rate - 0.3) * 0.5
    if text_fallback_used:
        score -= 0.15
    if category_fallback_used:
        score -= 0.10
    score = round(max(0.0, min(1.0, score)), 3)

    final_list = valid_keywords[:max_keywords]
    fallback_keywords = final_list[_ai_valid_boundary:]

    quality = Field653Quality(
        ai_raw_count=ai_raw_count,
        filtered_count=filtered_count,
        final_count=final_count,
        backup_used=backup_used,
        category_fallback_used=category_fallback_used,
        quality_score=score,
        flags=flags,
        fallback_keywords=fallback_keywords,
        raw_keywords=keywords,
        blocked_keywords=blocked_kws,
    )
    return "".join([f"$a{k}" for k in final_list]), quality


async def generate_653_subfield_line(
    meta: AladinMetadata653,
    max_keywords: int = 7,
    min_keywords: int = 5,
    settings: Settings | None = None,
    client: httpx.AsyncClient | None = None,  # 하위호환: 무시됨
) -> tuple[str | None, str | None, TokenUsage | None, Field653Quality | None]:
    """
    Returns (subfield_line, error, token_usage, quality).
    subfield_line: '$a키워드1$a키워드2…' 형식 (=653 접두 없음), 실패 시 None.
    quality: Field653Quality 품질 지표, 실패 시 None.
    """
    s = get_settings() if settings is None else settings
    if not s.openai_api_key:
        return None, "OPENAI_API_KEY가 설정되지 않았습니다.", None, None

    category = meta.category
    title = meta.title
    authors = clean_author_str(meta.authors)
    description = meta.description
    toc = meta.toc
    publisher_desc = meta.publisher_desc
    content_code = meta.content_code

    input_text = _build_input(
        category, title, authors, description, toc, max_keywords, publisher_desc,
        content_code=content_code,
    )
    try:
        raw, usage = await _call_learned_agent_api(input_text, s)
    except Exception as e:
        logger.exception("OpenAI 653 호출 실패")
        return None, str(e), None, None

    raw = _strip_preamble(raw)
    if _REFUSAL_RE.search(raw or ""):
        logger.warning("AI 거절 응답 감지, 1회 재시도: %.80s", raw)
        try:
            raw_retry, usage_retry = await _call_learned_agent_api(input_text, s)
        except Exception:
            logger.exception("재시도 호출 실패")
            raw_retry, usage_retry = "", None
        if usage_retry is not None:
            if usage is not None:
                usage = TokenUsage(
                    prompt_tokens=usage.prompt_tokens + usage_retry.prompt_tokens,
                    completion_tokens=usage.completion_tokens + usage_retry.completion_tokens,
                    total_tokens=usage.total_tokens + usage_retry.total_tokens,
                )
            else:
                usage = usage_retry
        raw = _strip_preamble(raw_retry)

    if usage is not None:
        usage.breakdown = _estimate_prompt_token_breakdown(
            input_text, category, title, authors, description, toc, publisher_desc,
            usage.prompt_tokens,
        )

    forbidden = build_forbidden_set(title, authors)
    if _REFUSAL_RE.search(raw or ""):
        logger.warning("AI 재시도에서도 거절 응답, fallback 처리: %.80s", raw)
        raw = ""
    kws = parse_keyword_line(raw)
    ai_output = "".join(f"$a{kw}" for kw in kws if should_keep_keyword(kw, forbidden))
    category_group = get_category_group(category, content_code)
    effective_min = 3 if category_group == "문학" else min_keywords
    subfield_line, quality = finalize_653(
        ai_output,
        forbidden,
        max_keywords=max_keywords,
        min_keywords=effective_min,
        category=category,
        toc=toc,
        description=description,
        content_code=content_code,
    )
    if not subfield_line:
        return None, "유효한 키워드를 추출하지 못했습니다.", usage, quality

    return subfield_line, None, usage, quality


def _estimate_prompt_token_breakdown(
    input_text: str,
    category: str,
    title: str,
    authors: str,
    description: str,
    toc: str,
    publisher_desc: str,
    prompt_tokens: int,
) -> dict[str, int]:
    """입력 프롬프트를 "지침"과 "도서 정보" 두 섹션으로 나눠 글자수 비율로
    prompt_tokens를 배분한 추정치. OpenAI가 섹션별 토큰수를 알려주지 않아서
    글자수 비율로 근사하며, 합계는 항상 실제 prompt_tokens와 같도록 맞춘다.
    """
    desc_trimmed = (description or "")[:600]
    toc_trimmed = (toc or "")[:300]
    pub_desc_trimmed = (publisher_desc or "")[:600]
    forbidden_list_len = len(", ".join(sorted(build_forbidden_set(title, authors))) or "(없음)")
    parts = [p.strip() for p in (category or "").split(">") if p.strip()]
    cat_tail_len = len(" ".join(parts[-2:]) if len(parts) >= 2 else (parts[-1] if parts else ""))

    book_data_len = (
        len(category or "") + cat_tail_len + len(title or "") + len(authors or "")
        + len(desc_trimmed) + len(toc_trimmed) + len(pub_desc_trimmed) + forbidden_list_len
    )
    total_len = len(_STATIC_INSTRUCTIONS) + len(input_text)
    if total_len <= 0 or prompt_tokens <= 0:
        return {}
    book_data_len = max(0, min(book_data_len, total_len))
    book_tokens = max(0, min(round(prompt_tokens * book_data_len / total_len), prompt_tokens))
    instruction_tokens = prompt_tokens - book_tokens
    return {"지침 프롬프트": instruction_tokens, "도서 정보(API·크롤링)": book_tokens}


def build_marc_653_line(subfield_line: str) -> str:
    """'$a..$a..' → MRK 한 줄(1215_main `=653  \\\\` + 서브필드 꼴)."""
    compact = subfield_line.replace(" ", "")
    return f"=653  \\\\{compact}"
