async def _call_learned_agent_api(
    input_text: str,
    settings: Settings,
    max_output_tokens: int = 200,
) -> tuple[str, TokenUsage | None]:
    """
    OpenAI Responses API 호출.
    instructions=_STATIC_INSTRUCTIONS 방식으로 매 요청마다 지침을 직접 전송.
    OpenAI 자동 프롬프트 캐싱으로 반복 전송 비용은 절감됨.
    """
    client = _get_openai_client(settings)
    try:
        resp = await client.responses.create(
            model=settings.openai_model,
            instructions=_STATIC_INSTRUCTIONS,
            input=input_text,
            max_output_tokens=max_output_tokens,
        )
    except Exception:
        logger.exception("OpenAI Responses API 호출 실패")
        raise
    content = (resp.output_text or "").strip()
    usage: TokenUsage | None = None
    if resp.usage:
        u = resp.usage
        usage = TokenUsage(
            prompt_tokens=u.input_tokens,
            completion_tokens=u.output_tokens,
            total_tokens=u.input_tokens + u.output_tokens,
        )
    return content, usage




_REFUSAL_RE = re.compile(
    r"i'?\s*m\s+sorry|i\s+cannot|i\s+can'?t|죄송|할\s*수\s*없|제공할\s*수",
    re.IGNORECASE,
)


def _strip_preamble(raw: str | None) -> str:
    """$a 앞에 붙은 AI 설명문을 제거하고 키워드 부분만 반환.

    AI가 "정보 부족으로 5개를 생성할 수 없습니다.\n$a키워드1 $a키워드2" 처럼
    응답할 때 설명문이 _REFUSAL_RE에 걸려 유효 키워드까지 버려지는 문제 방지.
    """
    if not raw:
        return ""
    idx = raw.find("$a")
    if idx > 0:
        return raw[idx:]
    return raw


def parse_keyword_line(raw: str) -> list[str]:
    """GPT 응답에서 $a… 패턴(및 백업 파싱)으로 키워드 나열."""
    pattern = re.compile(r"\$a(.*?)(?=(?:\$a|$))", re.DOTALL)
    kws = [m.group(1).strip() for m in pattern.finditer(raw)]
    if not kws:
        tmp = re.split(r"[,\n;|/·]", raw)
        kws = [t.strip().lstrip("$a") for t in tmp if t.strip()]
    kws = [kw.replace(" ", "") for kw in kws if kw]
    return kws


def _extract_backup_candidates(category: str, toc: str, description: str) -> list[str]:
    """GPT 결과가 부족할 때 보강 후보를 추출한다."""
    text = " ".join([category or "", toc or "", description or ""])
    tokens = re.findall(r"[가-힣A-Za-z]{2,12}", text)
    deny = {
        "목차", "차례", "서론", "결론", "저자", "작가", "소개", "연구", "방법", "이론", "문학",
        "한국", "세계", "도서", "작품", "출판", "분석", "개요", "현황", "의의", "시사점",
        "단순히", "구체적", "올바른", "작은", "변화", "모음", "상식", "추천",
        "활기찬", "품격있는", "품격노년", "치과의사팁", "비결", "팁",
        "오래", "사는", "아프지", "보내는",
        # 영문 섹션 표지 (목차·챕터 제목에서 파생)
        "Part", "Chapter", "Vol", "Section", "Unit", "Lesson",
        # 한국어 대명사·의문사
        "내가", "그가", "우리가", "무엇인가", "어떻게",
        # 동사 명사화 (문장에서 이탈한 형태)
        "찾기", "하기", "되기", "쓰기",
    }
    out: list[str] = []
    for t in tokens:
        w = t.replace(" ", "")
        if len(w) < 2 or len(w) > 10:
            continue
        if w in deny:
            continue
        # 한국어 어미·조사가 붙은 문장 조각 제거 — 강사는·훈련하는·흔들리면·
        # 없는·길의·나침반이·사람들처럼 추천사·본문 문장에서 잘려나온 토큰 방지.
        # "면" 종결만 3글자+ 요구(도면·평면·화면 등 2자 실제 명사 보호), 나머지는
        # 1자+ 어간도 차단 — 회의·정의·강의 같은 짧은 실제 명사가 드물게 같이
        # 걸릴 수 있지만, 문장 조각 노출을 막는 쪽이 이 최후 폴백 단계에서는 더 안전함.
        if re.search(r"^[가-힣]{1,}는$", w):
            continue
        if re.search(r"^[가-힣]{3,}면$", w):
            continue
        if re.search(r"^[가-힣]{1,}(의|이|가|을|를|은|들|과|와|며)$", w):
            continue
        out.append(w)
    return out


